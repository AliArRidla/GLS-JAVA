public class BintangPersegiPanjang {
    public static void main(String[] args) {
        int nilai = 6;
        for (int i = 0; i < nilai; i++) {
            System.out.print("-");
            for (int j = 0; j < 4; j++) {
                System.out.print("* ");    
            }
        System.out.print("\n");
        }
    }
}
